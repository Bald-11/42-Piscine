#include <unistd.h>

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	h;

	i = '0';
	while (i <= '9')
	{
		j = i + 1;
		while (j <= '9')
		{
			h = j + 1;
			while (h <= '9')
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &h, 1);
				if (i != '7')
					write(1, ", ", 2);
				h++;
			}
			j++;
		}
		i++;
	}
}
